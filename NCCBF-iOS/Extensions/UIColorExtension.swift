import UIKit

extension UIColor {
    class var sakuraPink: UIColor {
        // HEX color code: #e9417c
        return UIColor(red: 0.91, green: 0.25, blue: 0.49, alpha: 1.0)
    }
    
    class var matsuriGold: UIColor {
        // HEX color code: #c5a22e
        return UIColor(red: 0.77, green: 0.64, blue: 0.18, alpha: 1.0)
    }
}
